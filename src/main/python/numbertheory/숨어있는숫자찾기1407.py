a,b = map(int,input().split())

a -= 1

tmp_A = a
for i in range(1,50):
  tmp_A += (a//(2**i))*(2**i-2**(i-1))

tmp_B = b
for i in range(1,50):
  tmp_B += (b//(2**i))*(2**i-2**(i-1))

print(tmp_B - tmp_A)


# 설명

# N=12인 경우로 설명하겠습니다:
# range(2, int(12/2+1)) = range(2, 7)에서:
#
# i=2일 때:
#
# 2는 2,4,6,8,10,12의 약수
# 12//2 = 6 (등장횟수)
# 2*(6-1) = 10 (자기자신 제외)
# i=3일 때:
#
# 3은 3,6,9,12의 약수
# 12//3 = 4 (등장횟수)
# 3*(4-1) = 9 (자기자신 제외)
# i=4일 때:
#
# 4는 4,8,12의 약수
# 12//4 = 3 (등장횟수)
# 4*(3-1) = 8 (자기자신 제외)
# i=5일 때:
#
# 5는 5,10의 약수
# 12//5 = 2 (등장횟수)
# 5*(2-1) = 5 (자기자신 제외)
# i=6일 때:
#
# 6은 6,12의 약수
# 12//6 = 2 (등장횟수)
# 6*(2-1) = 6 (자기자신 제외)
# 최종 합: 10 + 9 + 8 + 5 + 6 = 38
#
# 이렇게 각 수가 약수로 등장하는 횟수를 한 번에 계산하여 처리합니다.
